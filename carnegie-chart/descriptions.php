    <div id="descriptions_group2" class="panel">
      <h6 class="italic center">Six Ways to Make People Like You</h6><br />
      <ul class="square">
        <div id="descriptions_group2_1">
          <li><span class="bold">Become genuinely interested in other people</span></li>
          <p class="desc">
            Care about them and their interests. Do things for others that require time, energy, unselfishness, and thoughtfulness.
            You can make more friends in 2 months by becoming genuinely interested in other people than you can in
            2 years trying to get people interested in you.
          </p>
        </div>
        <div id="descriptions_group2_2">
          <li><span class="bold">Smile</span></li>
          <p class="desc">
            A smile says "I like you. You make me happy. I am glad to see you."
            It's been said that "People who smile tend to manage, teach and sell more effectively, and to raise happier children."
            It's a simple enough idea, so who knows why so few people seem to follow it.
          </p>
        </div>
        <div id="descriptions_group2_3">
          <li><span class="bold">Use a person's name</span></li>
          <p class="desc">
            Remember that a person's name is to that person the sweetest and most important sound in any language.
            Make an effort to remember a person's name on the first try.
            Use it when directly referring to them, interacting with others about them, naming projects, etc.
          </p>
        </div>
        <div id="descriptions_group2_4">
          <li><span class="bold">Encourage others to talk about themselves</span></li>
          <p class="desc">
            Be an attentive listener. To be interesting, be interested. Ask questions that people will enjoy answering.
            Encourage them to talk about themselves and their accomplishments.
          </p>
        </div>
        <div id="descriptions_group2_5">
          <li><span class="bold">Talk in terms of the other person's interest</span></li>
          <p class="desc">
            Try and focus on what that person is interested in and talk about it. Franklin Roosevelt, before having a
            visitor in his office, used to study topics he knew his guest would be interested in discussing before they came over.
          </p>
        </div>
        <div id="descriptions_group2_6">
          <li><span class="bold">Make the other person feel important</span></li>
          <p class="desc">
            Always make the other person feel important. William James said, "The deepest principle in human nature is the craving to be appreciated."
            Help someone feel appreciated and how could they not like you?
          </p>
        </div>
      </ul>
    </div>
    <div id="descriptions_group4" class="panel">
      <h6 class="italic center">Be a Leader: How to Change People Without Giving Offense or Arousing Resentment</h6><br />
      <ul class="square">
        <div id="descriptions_group4_1">
          <li><span class="bold">Begin with praise and honest appreciation</span></li>
          <p class="desc">
            It is always more comfortable to hear something unpleasant about ourselves after we have heard some
            praise about one of our good points.
            A barber always lathers a man before he shaves him.
          </p>
        </div>
        <div id="descriptions_group4_2">
          <li><span class="bold">Call attention to people's mistakes indirectly</span></li>
          <p class="desc">
            Many people begin their criticism with sincere praise,
            but then follow it with the word "but" and end with a critical statement.
            Once someone hears the “but”, it makes them question the sincerity of the praise and that it was only put there to cushion the insult coming.
            Try replacing the word “but” with “and”, and reword your criticism a bit to fit the sentence.
          </p>
        </div>
        <div id="descriptions_group4_3">
          <li><span class="bold">Talk about your own mistakes before criticizing others</span></li>
          <p class="desc">
            It feels a lot better to hear someone talk about their faults and kind of "get down on your level"
            in a sense before they point out yours.
            Admitting one’s own mistakes – even when one has corrected them – can help convince someone to change their behavior.
          </p>
        </div>
        <div id="descriptions_group4_4">
          <li><span class="bold">Ask questions instead of giving direct orders</span></li>
          <p class="desc">
            Giving suggestions instead of giving orders saves a person’s pride and gives him a sense of importance.
            It encourages cooperation instead of rebellion.
            Asking questions instead of ordering someone around can make an order seem more palatable and often
            stimulates the creativity of the person’s you ask
          </p>
        </div>
        <div id="descriptions_group4_5">
          <li><span class="bold">Let the other person save face</span></li>
          <p class="desc">
            Don't belittle anyone, ever, especially in front of other people.
            "I have no right to say or do anything that diminishes a man in his own eyes.
            What matters is not what I think of him, but what he thinks of himself.
            Hurting a man in his dignity is a crime." – Antoine de Saint-Exupery.
          </p>
        </div>
        <div id="descriptions_group4_6">
          <li><span class="bold">Praise every improvement, no matter how small</span></li>
          <p class="desc">
            Praise the slightest improvement and praise every improvement. Be “hearty in your approbation and lavish in your praise.”
            Words of praise can change someone’s life, but give specific praise, not just short flattery.
            Abilities wither under criticism; they blossom under encouragement.
          </p>
        </div>
        <div id="descriptions_group4_7">
          <li><span class="bold">Give the other person a fine reputation to live up to</span></li>
          <p class="desc">
             If you want to improve a person in a certain respect, act as though that particular trait were already
             one of his or her outstanding characteristics.
             The average person can be led readily if you have his or her respect and if you show that
             you respect that person for some kind of ability.
             Give them public praise in front of coworkers or friends.
          </p>
        </div>
        <div id="descriptions_group4_8">
          <li><span class="bold">Use encouragement. Make the fault seem easy to correct.</span></li>
          <p class="desc">
            Praise someone’s good points and minimize the person’s faults.
            Let the other person know you have faith in them to get over that obstacle.
          </p>
        </div>
        <div id="descriptions_group4_9">
          <li><span class="bold">Make the other person happy about doing what you suggest</span></li>
          <p class="desc">
            Be empathetic. Ask yourself what it is the other person really wants.
            Be sincere. Do not promise anything you can’t deliver.
            When you make a request, put it in a form that shows the other person how they will benefit from it,
            and match those benefits to their wants.
          </p>
        </div>
      </ul>
