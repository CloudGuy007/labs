    <div id="descriptions_group1" class="panel">
      <h6 class="italic center">Fundamental Techniques in Handling People</h6><br />
      <ul class="square">
        <div id="descriptions_group1_1">
          <li><span class="bold">Don't criticize, condemn, or complain</span></li>
          <p class="desc">
            Criticism is often futile and merely puts people on the defensive. Instead try to understand why people do what they do.
          </p>
        </div>
        <div id="descriptions_group1_2">
          <li><span class="bold">Give honest and sincere appreciation</span></li>
          <p class="desc">
            If you can arouse enthusiasm in people and a sense of good will, you can begin to realize the true power of appreciation.
            Be anxious to praise and loath to find fault, but be sincere in your appreciation.
          </p>
        </div>
        <div id="descriptions_group1_3">
          <li><span class="bold">Arouse in the other person an eager want</span></li>
          <p class="desc">
            Henry Ford said that "If there is any one secret of success, it lies in the ability to get the other person's
            point of view and see things from that person's angle as well as from your own."
            Put the other person’s wants before your own, and convince them how they can benefit.
          </p>
        </div>
      </ol>
    </div>
    <div id="descriptions_group3" class="panel">
      <h6 class="italic center">How to Win People to Your Way of Thinking</h6><br />
      <ul class="square">
        <div id="descriptions_group3_1">
          <li><span class="bold">Avoid argumentation</span></li>
          <p class="desc">
            Avoid arguments like you would rattlesnakes or earthquakes.
            Most of the time, they’ll just make someone feel embarrassed, uncomfortable, or hurt their pride and make them feel inferior to you.
            Sure, you may achieve a victory sometimes; but it will be an empty victory because you will never get your opponent's good will.
          </p>
        </div>
        <div id="descriptions_group3_2">
          <li><span class="bold">Never say "You're wrong"</span></li>
          <p class="desc">
            Telling someone they’re wrong is a direct blow at their intelligence, pride and self-respect.
            It doesn’t make them want to agree with you, but strike back.
            If you are going to prove anything, try not to let anyone know about it.
            Do it subtly and adroitly so that no one knows you’re doing it.
          </p>
        </div>
        <div id="descriptions_group3_3">
          <li><span class="bold">If you are wrong, admit it quickly and emphatically</span></li>
          <p class="desc">
            Any fool can try to defend his mistakes, but it raises one above the herd to admit one's mistakes.
            Be humble by saying derogatory things about yourself you know the other person wants to say or intends to say –
            chances are they will then have a forgiving attitude towards you and minimize your mistakes in their minds.
          </p>
        </div>
        <div id="descriptions_group3_4">
          <li><span class="bold">Begin in a friendly way</span></li>
          <p class="desc">
            If your temper is aroused and you go off on someone and tell them a thing or two, you might feel good afterwards,
            but how does that person feel? Do they want to agree with your points after you embarrassed them and attacked their pride?
            Begin a difficult conversation calmly, and things will turn out much better.
          </p>
        </div>
        <div id="descriptions_group3_5">
          <li><span class="bold">Get the other person saying "yes, yes" immediately</span></li>
          <p class="desc">
            Ask questions which your opponent is forced to agree with (yes!).
            Keep on winning one admission after another until you have an armful of yeses to build upon,
            making your opponent possibly want to conclude with your side being right instead of their own.
            Once in the “no” state, it takes a LOT of effort and wisdom to try and transform that bristling negative into an affirmative.
          </p>
        </div>
        <div id="descriptions_group3_6">
          <li><span class="bold">Let the other person do a great deal of talking</span></li>
          <p class="desc">
            Most people trying to win others to their way of thinking do too much talking themselves.
            Let the other person talk themselves out.
            They know a lot more about their business and problems than you do. So ask them questions and hear them out.
          </p>
        </div>
        <div id="descriptions_group3_7">
          <li><span class="bold">Let the other person feel that the idea is his or hers</span></li>
          <p class="desc">
            Most people prefer to feel that they are acting on their own ideas or buying on their own accord, not told or sold something.
            Feeling it was their idea will make them "want" to do it more.
          </p>
        </div>
        <div id="descriptions_group3_8">
          <li><span class="bold">See things from the other's point of view</span></li>
          <p class="desc">
            Try to think through that person’s point of view and think why someone should want to adapt to your point of view,
            and also how they would like to hear what you are saying.
          </p>
        </div>
        <div id="descriptions_group3_9">
          <li><span class="bold">Be sympathetic with others' ideas and desires</span></li>
          <p class="desc">
            Three-fourths of the people you meet want sympathy. Give it to them and they will love you.
            One phrase that eliminates ill will, creates good will, and gets people listening to you better:
            "I don’t blame you one bit for feeling as you do. If I were you I would undoubtedly feel just as you do."
          </p>
        </div>
        <div id="descriptions_group3_10">
          <li><span class="bold">Appeal to the nobler motives</span></li>
          <p class="desc">
            Everybody usually has two reasons for doing anything: one that sounds good, and then their real reason.
            The person may know the real reason, but you don’t need to emphasize that.
            Instead, try appealing to a nobler cause.
            Show them good motives behind agreeing to what you're trying to convince them.
          </p>
        </div>
        <div id="descriptions_group3_11">
          <li><span class="bold">Dramatize your ideas</span></li>
          <p class="desc">
            The truth has to be vivid, interesting, dramatic; you have to use showmanship. Do this if you want attention.
            This does not mean lying, but saying something that dramatizes the importance of something that you’re
            talking about or trying to convey.
          </p>
        </div>
        <div id="descriptions_group3_12">
          <li><span class="bold">Throw down a challenge</span></li>
          <p class="desc">
            Stimulate competition, not in a sordid money-getting way, but in the desire to excel.
            People love the chance to express themselves, their worth, and to show their importance.
          </p>
        </div>
      </ul>
